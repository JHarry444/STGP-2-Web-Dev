"use strict";

console.log("6 + 6 =", 6 + 6);
console.log("6 - 6 =", 6 - 6);
console.log("6 * 6 =", 6 * 6);
console.log("6 / 6 =", 6 / 6);
console.log("6 ** 6 =", 6 ** 6);
console.log("10 % 6 =", 10 % 6);

// concatenation
console.log("27" + "27");

console.log("Jordan" + " "  + "Harrison");

// type coercion
console.log("27" * "27");

console.log("Jordan" * "Harrison");

console.log("NaN:", typeof NaN);

// Boolean
console.log(true && false);

console.log(true || false);

console.log(2 * 2 === 4 && 5 > 3);

console.log(2 * 2 === 4 && 5 > 6);

console.log(2 * 2 === 4 || 5 > 6);

console.log(!true);
console.log(!false);

let name = "Steve";

let me = `Hello, my name is ${name}`;

console.log("Me:", me);

let num = 12;

console.log("Num:", num);

console.log("Num++:", num++);

console.log("Num:", num);

console.log("++Num:", ++num);

console.log("Num:", num);

console.log("Num--:", num--);

console.log("Num:", num);

console.log("--Num:", --num);

console.log("Num:", num);

console.log("Num += 12:", num += 12);
console.log("Num -= 12:", num -= 12);
console.log("Num *= 12:", num *= 12);
console.log("Num /= 12:", num /= 12);
console.log("Num %= 7:", num %= 7);
console.log("Num **= 7:", num **= 7);